import {Date} from '../../labs/lab05/Date'
import {expect} from 'chai'

describe('Date', function () {

    describe('', function () {
        it('should ', function () {
            expect(Date.yearsInTimestamp(3046)).eql(8)
        })
        it('should ', function () {
            expect(Date.monthsInTimestamp(3046)).eql(4)

        })
        it('should ', function () {
            expect(Date.daysInTimestamp(36203)).eql(12)
        })

    })

})